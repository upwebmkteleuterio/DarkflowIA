// --- APP SPECIFIC TYPES ---

/**
 * Represents a trending topic discovered by Trend Hunter
 */
export interface Trend {
  id: string;
  topic: string;
  reason: string;
  marketGap: string;
  viralScore: number;
  sources: { title: string; uri: string }[];
}

/**
 * Represents a title idea generated by the Title Generator
 */
export interface TitleIdea {
  id: string | number;
  title: string;
  ctrScore: string;
  tags: string[];
}

/**
 * Supported batch operation types
 */
export type BatchType = 'script' | 'thumbnail';

/**
 * Task definition for the batch processing engine
 */
export interface BatchTask {
  id: string;
  itemId: string;
  projectId: string;
  type: BatchType;
  status: 'pending' | 'processing' | 'completed' | 'failed' | 'cancelled';
  config?: any;
  error?: string;
}

/**
 * Global state of the batch processing system
 */
export interface BatchState {
  tasks: BatchTask[];
  isProcessing: boolean;
  currentTaskId: string | null;
  stats: {
    total: number;
    completed: number;
    failed: number;
    cancelled: number;
    percent: number;
  };
}
